import React, { Component, PropTypes } from 'react';
import { connect } from 'thet-react'
import { Act } from '../store'
import Picker from '../components/Picker';
import Posts from '../components/Posts';

class App extends Component {
  constructor(props) {
    super(props);
    this.handleChange = this.handleChange.bind(this)
    this.handleRefreshClick = this.handleRefreshClick.bind(this)
  }

  handleChange(category) {
    this.props.changeCategory(category);
  }

  handleRefreshClick(event) {
    event.preventDefault();
    const { invalidate, category } = this.props;
    invalidate(category);
  }

  render() {
    const { category, posts, isFetching, lastUpdated } = this.props;
    return (
      <div>
        <Picker
          value={category}
          onChange={this.handleChange}
          options={['reactjs', 'frontend']}
        />
        <p>
          {lastUpdated &&
            <span>
              Last updated at {new Date(lastUpdated).toLocaleTimeString()}.
              {' '}
            </span>
          }
          {!isFetching &&
            <a href="#" onClick={this.handleRefreshClick}>
              Refresh
            </a>
          }
        </p>

        {isFetching && posts.length === 0 &&
          <h2>Loading...</h2>
        }
        {!isFetching && posts.length === 0 &&
          <h2>Empty.</h2>
        }
        {posts.length > 0 &&
          <div style={{ opacity: isFetching ? 0.5 : 1 }}>
            <Posts posts={posts} />
          </div>
        }
      </div>
    );
  }
}

App.propTypes = {
  category: PropTypes.string.isRequired,
  posts: PropTypes.array.isRequired,
  isFetching: PropTypes.bool.isRequired,
  lastUpdated: PropTypes.number,
  changeCategory: PropTypes.func.isRequired,
  invalidate: PropTypes.func.isRequired,
};

const mapProps = send => () => {
  const category = send(Act.getCurrentCategory)
  const {
    posts,
    isFetching,
    lastUpdated,
  } = send(Act.getOrInitReddit, category)
  return {
    category,
    posts,
    isFetching,
    lastUpdated,
    changeCategory: send.to(Act.changeCategory),
    invalidate: send.to(Act.invalidateCategory),
  }
}

export default connect(App, { mapProps });
